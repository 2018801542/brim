        <div class="row" style="margin-bottom:50px;margin-left:50px">
           
               <h3>The interesting element of BRiM is on the highlight of the level of potential business risks that requires more attention and analysis by the users. How BRiM works? BRiM computes scores for parameters of external and internal risks. In particular, the external risks scores will be measured based on (1) Compliant to regulations, (2) Ecosystem, and (3) External Environment. While the internal risks scores will be measured based on five elements namely (1) Financial; (2) Operational; (3) Workforce; (4) Strategic; and (5) Governance.  </h3> 
               
