               <h3>BRiM is a business solution to help entrepreneurs/business evaluate their business risk exposure in a dynamic economic environment. As risk assessment has become an essential part of business strategies to detect and mitigate the potential threat to the business, the development of BRiM aims to provide a practical tool for business solutions. BRiM is useful for small and medium businesses as it measures the business risks from perspectives of external and internal 
                   potential events that might affect business operations. The business risks will be comprised of 
                   financial and non-financial measures. </h3> 
               
            </div>
